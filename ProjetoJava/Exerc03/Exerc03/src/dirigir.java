public class dirigir{
    int numMarcha;
    String carro;
    int cavalos;

    dirigir(){
        this.numMarcha = 0;
        this.carro = "";
        this.cavalos = 0;

    }
    dirigir(int numMarcha, String carro, int cavalos){
        this.numMarcha = numMarcha;
        this.carro = carro;
        this.cavalos = cavalos;
        
    }
}